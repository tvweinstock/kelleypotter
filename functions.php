/* is_blog() - checks various conditionals to figure out if you are currently within a blog page */
function is_blog () {
	global  $post;
	$posttype = get_post_type($post );
	return ( ((is_archive()) || (is_author()) || (is_category()) || (is_home()) || (is_single()) || (is_tag())) && ( $posttype == 'post')  ) ? true : false ;
}

/* get_post_parent() - Returns the current posts parent, if current post if top level, returns itself */
function get_post_parent($post) {
	if ($post->post_parent) {
		return $post->post_parent;
	}
	else {
		return $post->ID;
	}
}

/* get_post_thumbnail_url() - Returns the post thumbnail URL without any goofin' around */ 

function get_post_thumbnail_url($size = 'thumbnail', $image_id = NULL ) {
	if ($image_id === NULL) {
		$image_id = get_post_thumbnail_id();  
	}
	$image_url = wp_get_attachment_image_src($image_id,$size);  
	$image_url = $image_url[0];
	return $image_url;
} 